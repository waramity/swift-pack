import SwiftUI

class SeeFoodImageClassifier: ObservableObject {
    
    @Published private var classifier = SeeFoodClassifier()
    
    var imageClass: String? {
        classifier.results
    }
        
    // MARK: Intent(s)
    func detect(uiImage: UIImage) {
        guard let ciImage = CIImage (image: uiImage) else { return }
        classifier.detect(ciImage: ciImage)
        
    }
        
}
